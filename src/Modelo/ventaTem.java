package Modelo;

/**
 *
 * @author nicolasgarcia
 */
public class ventaTem {
    String producto;
    int cantidad;
    int valor;

    public ventaTem() {
    }

    public ventaTem(String producto, int cantidad, int valor) {
        this.producto = producto;
        this.cantidad = cantidad;
        this.valor = valor;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
}
